#include "minishell.h"

void	print_error(char *cmd, char *msg)
{
	ft_putstr_fd("minishell: ", 2);
	if (cmd)
	{
		ft_putstr_fd(cmd, 2);
		ft_putstr_fd(": ", 2);
	}
	ft_putendl_fd(msg, 2);
}

void	exit_error(char *msg, int exit_code)
{
	print_error(NULL, msg);
	exit(exit_code);
}
